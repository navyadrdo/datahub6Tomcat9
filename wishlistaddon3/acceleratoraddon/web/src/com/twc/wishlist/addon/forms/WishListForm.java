public class WishListForm
{
	public String wishlistname = null;
	public String wishlistcomment = null;

	/**
	 * @return the wishlistname
	 */
	public String getWishlistname()
	{
		return wishlistname;
	}

	/**
	 * @param wishlistname
	 *           the wishlistname to set
	 */
	public void setWishlistname(final String wishlistname)
	{
		this.wishlistname = wishlistname;
	}

	/**
	 * @return the wishlistcomment
	 */
	public String getWishlistcomment()
	{
		return wishlistcomment;
	}

	/**
	 * @param wishlistcomment
	 *           the wishlistcomment to set
	 */
	public void setWishlistcomment(final String wishlistcomment)
	{
		this.wishlistcomment = wishlistcomment;
	}


}
